public class Database {

	int ID;
	String Name;
	int Age;
	String Department;
	String Designation;
	String ReportingTo;
	
	public Database(int ID, String Name, int Age, String Department, String Designation, String ReportingTo) {
		this.ID = ID;
		this.Name = Name;
		this.Age = Age;
		this.Department = Department;
		this.Designation = Designation;
		this.ReportingTo = ReportingTo;
	}
}
